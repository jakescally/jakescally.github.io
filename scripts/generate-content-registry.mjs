import { promises as fs } from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const contentRoot = path.join(projectRoot, "src", "content");
const outputFile = path.join(projectRoot, "src", "generated", "content.generated.ts");
const collections = [
  { name: "blog", dir: path.join(contentRoot, "blog") },
  { name: "projects", dir: path.join(contentRoot, "projects") },
  { name: "photoSets", dir: path.join(contentRoot, "photoSets") },
  { name: "drinks", dir: path.join(contentRoot, "drinks") }
];
const supportedExtensions = new Set([".md", ".mdx"]);

function toPosix(value) {
  return value.split(path.sep).join("/");
}

function toIdentifier(relativePath) {
  return relativePath
    .replace(/\.[^.]+$/, "")
    .split(/[\\/]/)
    .map((segment) => segment.replace(/[^a-zA-Z0-9]/g, "_"))
    .join("_")
    .replace(/_+/g, "_");
}

function toId(filename) {
  return filename.replace(/\.(md|mdx)$/, "");
}

async function getCollectionFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });

  return entries
    .filter((entry) => entry.isFile() && supportedExtensions.has(path.extname(entry.name)))
    .map((entry) => path.join(dir, entry.name))
    .sort((left, right) => left.localeCompare(right));
}

async function main() {
  const importLines = [];
  const exportBlocks = [];

  for (const collection of collections) {
    const files = await getCollectionFiles(collection.dir);
    const items = [];

    for (const file of files) {
      const relativeFromOutput = toPosix(path.relative(path.dirname(outputFile), file));
      const relativeFromCollection = toPosix(path.relative(collection.dir, file));
      const identifier = `${collection.name}_${toIdentifier(relativeFromCollection)}`;
      const frontmatterIdentifier = `${identifier}_frontmatter`;
      const id = toId(path.basename(file));

      importLines.push(
        `import ${identifier}, { frontmatter as ${frontmatterIdentifier} } from "${relativeFromOutput}";`
      );
      items.push(
        `  { id: "${id}", default: ${identifier}, frontmatter: ${frontmatterIdentifier} },`
      );
    }

    exportBlocks.push(
      `export const ${collection.name}Modules = [\n${items.join("\n")}\n];`
    );
  }

  const fileContents = `// This file is generated by scripts/generate-content-registry.mjs. Do not edit manually.
${importLines.join("\n")}

${exportBlocks.join("\n\n")}
`;

  await fs.mkdir(path.dirname(outputFile), { recursive: true });
  await fs.writeFile(outputFile, fileContents);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
