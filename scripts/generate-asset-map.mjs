import { promises as fs } from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const assetsRoot = path.join(projectRoot, "src", "assets");
const outputFile = path.join(projectRoot, "src", "generated", "assets.generated.ts");
const supportedExtensions = new Set([
  ".avif",
  ".gif",
  ".jpeg",
  ".jpg",
  ".png",
  ".webp"
]);

async function walk(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      files.push(...await walk(fullPath));
      continue;
    }

    const extension = path.extname(entry.name).toLowerCase();

    if (supportedExtensions.has(extension)) {
      files.push(fullPath);
    }
  }

  return files;
}

function toPosix(value) {
  return value.split(path.sep).join("/");
}

function toIdentifier(relativePath) {
  const stem = relativePath
    .replace(/\.[^.]+$/, "")
    .split(/[\\/]/)
    .map((segment) => segment.replace(/[^a-zA-Z0-9]/g, "_"))
    .join("_");
  const extension = path.extname(relativePath).replace(/[^a-zA-Z0-9]/g, "");

  return `asset_${stem}_${extension}`.replace(/_+/g, "_");
}

async function main() {
  const assetFiles = (await walk(assetsRoot)).sort((left, right) => left.localeCompare(right));
  const importLines = [];
  const mapLines = [];

  for (const assetFile of assetFiles) {
    const relativeFromAssets = toPosix(path.relative(assetsRoot, assetFile));
    const relativeImportPath = toPosix(path.relative(path.dirname(outputFile), assetFile));
    const identifier = toIdentifier(relativeFromAssets);

    importLines.push(`import ${identifier} from "${relativeImportPath}";`);
    mapLines.push(`  ["${relativeFromAssets}", ${identifier}],`);
  }

  const fileContents = `import type { ImageMetadata } from "astro";

// This file is generated by scripts/generate-asset-map.mjs. Do not edit manually.
${importLines.join("\n")}

export const assetMap = new Map<string, ImageMetadata>([
${mapLines.join("\n")}
]);
`;

  await fs.mkdir(path.dirname(outputFile), { recursive: true });
  await fs.writeFile(outputFile, fileContents);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
